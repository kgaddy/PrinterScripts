package com.programqii.printercontrol.services.pojo;

public class Coordinate {
	private float x;
	private float y;
	private float z;
	private float e;

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

	public float getZ() {
		return z;
	}

	public void setZ(float z) {
		this.z = z;
	}

	public float getE() {
		return e;
	}

	public void setE(float e) {
		this.e = e;
	}
}
